def seed_documents():
    db: Session = SessionLocal()
    embedding_service = EmbeddingService()
    
    try:
        # 1. 確保 RAG 資料表已創建 (防禦性再次確認)
        Base.metadata.create_all(bind=engine)
        
        # 2. 清除舊的 document 數據以保證 Seeder 可重複執行 (Idempotent)
        print("🧹 正在清除舊的 RAG 文件數據...")
        db.query(DocumentChunk).delete()
        db.query(Document).delete()
        db.commit()
        
        print("🌱 開始匯入 RAG 種子政策與科普知識文本...")
        
        for doc_data in POLICY_DOCUMENTS:
            print(f"📖 處理文檔: {doc_data['title']}")
            
            # 建立 Document
            doc = Document(
                title=doc_data["title"],
                source_type=doc_data["source_type"],
                source_url=doc_data["source_url"]
            )
            db.add(doc)
            db.flush() # 取得 doc.id
            
            # 處理 Chunks
            for idx, content in enumerate(doc_data["content_blocks"]):
                print(f"  ⚡ 向量化 Chunk {idx+1}/{len(doc_data['content_blocks'])}...")
                # 獲取 768 維 Embedding 向量
                vec = embedding_service.get_embedding(content)
                
                chunk = DocumentChunk(
                    document_id=doc.id,
                    chunk_index=idx,
                    content=content,
                    embedding=vec
                )
                db.add(chunk)
                
        db.commit()
        print("🎉 RAG 種子文檔匯入成功！")
        
    except Exception as e:
        db.rollback()
        print(f"❌ [Seeder] 匯入失敗: {str(e)}")
        raise e
    finally:
        db.close()

if __name__ == "__main__":
    seed_documents()
