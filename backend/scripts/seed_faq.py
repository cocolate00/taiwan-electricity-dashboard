def seed_faq():
    db: Session = SessionLocal()
    embedding_service = EmbeddingService()
    
    try:
        # 1. 確保 RAG 資料表已創建
        Base.metadata.create_all(bind=engine)
        
        # 2. 尋找或建立 "常見電力問答集" Document (避免刪除其他 policy / report 文檔)
        print("🔍 尋找舊的 FAQ 主文件...")
        faq_doc = db.query(Document).filter(Document.source_type == "faq").first()
        
        if faq_doc:
            print(f"🧹 刪除舊的 FAQ Chunks (Document ID: {faq_doc.id})...")
            db.query(DocumentChunk).filter(DocumentChunk.document_id == faq_doc.id).delete()
            db.commit()
        else:
            print("🌱 建立新的 FAQ 主文件...")
            faq_doc = Document(
                title="常見電力問答集",
                source_type="faq",
                source_url="#"
            )
            db.add(faq_doc)
            db.flush() # 取得 faq_doc.id
            db.commit()
            
        print(f"🌱 開始匯入 {len(FAQ_ITEMS)} 筆 FAQ 常見問答對並進行向量化...")
        
        for idx, item in enumerate(FAQ_ITEMS):
            q_text = item["question"]
            a_text = item["answer"]
            print(f"  ⚡ 處理 FAQ {idx+1}/{len(FAQ_ITEMS)}: {q_text[:20]}...")
            
            # 使用問題文字來產生 3072 維 Embedding 向量
            vec = embedding_service.get_embedding(q_text)
            
            # 存入主要問題
            chunk = DocumentChunk(
                document_id=faq_doc.id,
                chunk_index=idx * 2,  # 確保 index 唯一
                content=a_text,
                embedding=vec
            )
            db.add(chunk)

            # 如果有 alternative question，也建立一個 chunk 映射到同一個回答，以增加匹配度
            if "question_alternative" in item:
                alt_text = item["question_alternative"]
                print(f"  ⚡ 處理 FAQ Alternative: {alt_text[:20]}...")
                alt_vec = embedding_service.get_embedding(alt_text)
                alt_chunk = DocumentChunk(
                    document_id=faq_doc.id,
                    chunk_index=idx * 2 + 1,
                    content=a_text,
                    embedding=alt_vec
                )
                db.add(alt_chunk)
            
        db.commit()
        print("🎉 FAQ 常見問答對匯入成功！")
        
    except Exception as e:
        db.rollback()
        print(f"❌ [FAQ Seeder] 匯入失敗: {str(e)}")
        raise e
    finally:
        db.close()

if __name__ == "__main__":
    seed_faq()
